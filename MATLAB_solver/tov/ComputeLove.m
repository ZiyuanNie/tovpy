function varargout = ComputeLove(c,y,l,parity)

%ComputeLove Calculate Love numbers
%   Given the compactness c, y = R H(R)'/H(R) and the multipole index
%   l, compute even and odd Love numbers
%
%   [kl,hl] = ComputeLove(c,y,l,'even')
%   jl      = ComputeLove(c,y,l,'odd')
%
%   Refs
%   T.Hinderer, Astrophys.J.677:1216-1220, 2008 http://arxiv.org/abs/0711.2420
%   T.Damour & A.Nagar, Phys.Rev.D80:084035,2009
%   T.Binnington & E.Poisson, Phys.Rev.D80:084018,2009
%


% Call even/odd
if strcmp(parity,'even')
    
    if l>1    
        [kl,hl] = LoveEven(c,y,l);
    else
        kl = 0;
        hl = 0;
    end
    
    varargout{1} = kl;
    varargout{2} = hl;
    
elseif ismember(parity,'odd')
    
    if l>1
        jl = LoveOdd(c,y,l);
    else
        jl = 0;
    end
    
    varargout{1} = jl;
    
else
    error('unknown parity');
end




function [k,h] = LoveEven(c,y,l)
% Even parity
% Apsidal constant
% Shape Love number (suitably corrected to match the Newtonian)

% Shorthand
c2 = c.^2;
c3 = c.^3;
c4 = c.^4;
c5 = c.^5;
c6 = c.^6;
c7 = c.^7;
c8 = c.^8;
c9 = c.^9;
c10 = c.^10;
c11 = c.^11;
c13 = c.^13;
c15 = c.^15;
c17 = c.^17;

k = 0;
h = 0;

switch l
    
    case 2
        
        nk = (1-2*c)^2*(2+2*c*(y-1)-y);
        dk = 2*c*(6-3*y+3*c*(5*y-8))+4*c3*(13-11*y+c*(3*y-2)+2*c2*(1+y))...
            + 3*(1-2*c)^2*(2-y+2*c*(y-1))*log(1-2*c);
        
        nh = (-2 + 6*c + 2*c3*(1 + y) - c2*(6 + y));
        dh = (2*c*(6 + c2*(26 - 22*y) - 3*y + 4*c4*(1 + y) + 3*c*(-8 + 5*y) + c3*(-4 + 6*y))...
            - 3*(1 - 2*c)^2*(2 + 2*c*(-1 + y) - y)*log(1/(1 - 2*c)));
        
        k = 8*c5/5*nk/dk;
        h = -8*c5*nh/dh;
        
    case 3
        
        nk = (1 - 2*c)^2*(-3 - 3*c*(-2 + y) + 2*c2*(-1 + y) + y);
        dk = 2*c*(15*(-3 + y) + 4*c5*(1 + y) - 45*c*(-5 + 2*y) - 20*c3*(-9 + 7*y) + 2*c4*(-2 + 9*y) + 5*c2*(-72 + 37*y)) ...
            - 15*(1 - 2*c)^2*(-3 - 3*c*(-2 + y) + 2*c^2*(-1 + y) + y)*log(1/(1 - 2*c));
        
        nh = -5 + 15*c + 2*c3*(1 + y) - c2*(12 + y);
        dh = (5.*(2*c*(15*(-3 + y) + 4*c5*(1 + y) - 45*c*(-5 + 2*y) - 20*c3*(-9 + 7*y) ...
            + 2*c4*(-2 + 9*y) + 5*c2*(-72 + 37*y)) - 15*(1 - 2*c)^2*(-3 - 3*c*(-2 + y) + 2*c2*(-1 + y) + y)*log(1/(1 - 2*c))));
        
        k = 8*c7/7*nk/dk;
        h = 16*c7*nh/dh;
        
    case 4
        
        nk = (1 - 2*c)^2*(-7*(-4 + y) + 28*c*(-3 + y) - 34*c2*(-2 + y) + 12*c3*(-1 + y));
        dk = (2*c*(c2*(5360 - 1910*y) + c4*(1284 - 996*y) - 105*(-4 + y) + 8*c6*(1 + y) + 105*c*(-24 + 7*y) ...
            + 40*c3*(-116 + 55*y) + c5*(-8 + 68*y)) - 15*(1 - 2*c)^2*(-7*(-4 + y) + 28*c*(-3 + y) - 34*c2*(-2 + y) + 12*c3*(-1 + y))*log(1/(1 - 2*c)));
        
        nh = -9 + 27*c + 2*c3*(1 + y) - c2*(20 + y);
        dh = (21.*(2*c*(c2*(5360 - 1910*y) + c4*(1284 - 996*y) - 105*(-4 + y) + 8*c6*(1 + y) + 105*c*(-24 + 7*y) ...
            + 40*c3*(-116 + 55*y) + c5*(-8 + 68*y)) - 15*(1 - 2*c)^2*(-7*(-4 + y) + 28*c*(-3 + y) ...
            - 34*c2*(-2 + y) + 12*c3*(-1 + y))*log(1/(1 - 2*c))));
        
        k = 32*c9/147*nk/dk;
        h = -64*c9*nh/dh;
        
    case 5
        
        nk = (32*(1 - 2*c)^2*c11*(3*(-5 + y) - 15*c*(-4 + y) + 26*c2*(-3 + y) - 18*c3*(-2 + y) + 4*c4*(-1 + y)));
        dk = 99.*(2*c*(315*(-5 + y) + 8*c7*(1 + y) - 315*c*(-35 + 8*y) + 4*c6*(-2 + 27*y) - 56*c5*(-60 + 47*y) - 210*c3*(-170 + 57*y) + 105*c2*(-278 + 75*y) ...
            + 56*c4*(-345 + 158*y)) - 105*(1 - 2*c).^2*(3*(-5 + y) - 15*c*(-4 + y) + 26*c2*(-3 + y) - 18*c3*(-2 + y) + 4*c4*(-1 + y))*log(1/(1 - 2*c)));
        
        k = nk/dk;
        
    case 6
        
        nk = (1024*(1 - 2*c)^2*c13*(-33*(-6 + y) + 198*c*(-5 + y) - 444*c2*(-4 + y) + 456*c3*(-3 + y) - 208*c4*(-2 + y) + 32*c5*(-1 + y)));
        dk = 14157.*(2*c*(-3465*(-6 + y) + 32*c8*(1 + y) + 10395*c*(-16 + 3*y) + 16*c7*(-2 + 39*y) + 2016*c5*(-122 + 55*y) - 64*c6*(-457 + 362*y) - 210*c2*(-2505 + 541*y) ...
            + 210*c3*(-3942 + 1015*y) - 84*c4*(-7917 + 2567*y)) ...
            - 105*(1 - 2*c)^2*(-33*(-6 + y) + 198*c*(-5 + y) - 444*c2*(-4 + y) + 456*c3*(-3 + y) - 208*c4*(-2 + y) + 32*c5*(-1 + y))*log(1/(1 - 2*c)));
        
        k = nk/dk;
        
    case 7
        
        nk = 1024*(1 - 2*c)^2*c15*(143*(-7 + y) - 1001*c*(-6 + y) + 2750*c2*(-5 + y) - 3740*c3*(-4 + y) + 2600*c4*(-3 + y) - 848*c5*(-2 + y) + 96*c6*(-1 + y));
        dk = 20449.*(2*c*(45045*(-7 + y) + 160*c9*(1 + y) - 45045*c*(-63 + 10*y) + 80*c8*(-2 + 53*y) - 432*c7*(-651 + 521*y) - 4620*c3*(-4333 + 902*y) ...
            + 1155*c2*(-9028 + 1621*y) + 96*c6*(-33964 + 15203*y) + 126*c4*(-168858 + 42239*y) - 84*c5*(-144545 + 45971*y)) ...
            - 315*(1 - 2*c)^2*(143*(-7 + y) - 1001*c*(-6 + y) + 2750*c2*(-5 + y) - 3740*c3*(-4 + y) ...
            + 2600*c4*(-3 + y) - 848*c5*(-2 + y) + 96*c6*(-1 + y))*log(1/(1 - 2*c)));
        
        k = nk/dk;
        
    case 8
        
        nk = (16*(1 - 2*c)^2*(2*c*(c*(2*c*(2*c*(-737*(-4 + y) + 374*c*(-3 + y) - 92*c2*(-2 + y) +...
            8*c^3*(-1 + y)) + 1573*(-5 + y)) - 1859*(-6 + y)) + 572*(-7 + y)) - 143*(-8 + y)));
        dk = (286*c*(4*(90090 + c*(-900900 + c*(3768765 + c*(-8528520 + c*(11259633 + 2*c*(-4349499 + ...
            c*(1858341 + 8*c*(-47328 + c*(3092 + (-1 + c)*c))))))))) + (-1 + c)*(-1 + 2*c)*(-45045 + ...
            4*c*(90090 + c*(-285285 + c*(450450 + c*(-365211 + 4*c*(34881 + c*(-4887 + 2*c*(36 + c))))))))*...
            y) - 45045*(1 - 2*c)^2*(2*c*(c*(2*c*(2*c*(-737*(-4 + y) + 374*c*(-3 + y) - 92*c2*(-2 + y) + ...
            8*c^3*(-1 + y)) + 1573*(-5 + y)) - 1859*(-6 + y)) + 572*(-7 + y)) - 143*(-8 + y))*log(1/(1 - 2*c)));
        
        k = 256/2431 * c17 * nk/dk;
        
    otherwise
        
        k = ComputeLegendre(c,y,l);
        
end



function j = LoveOdd(c,y,l)
% Odd parity

j = zeros(size(l));

% Shorthand
c2 = c.^2;
c3 = c.^3;
c4 = c.^4;
c5 = c.^5;
c6 = c.^6;
c7 = c.^7;
c8 = c.^8;
c9 = c.^9;
c10 = c.^10;
c11 = c.^11;
c13 = c.^13;
c15 = c.^15;

j = 0;

switch l
    
    case 2
        
        nj =  96*c5*(-1 + 2*c)*(-3 + y);
        dj =  5.*(2*c*(9 + 3*c*(-3 + y) + 2*c2*(-3 + y) + 2*c3*(-3 + y)...
            - 3*y + 12*c4*(1 + y)) + 3*(-1 + 2*c)*(-3 + y)*log(1 - 2*c));
        
        j = nj/dj;
    
    %otherwise
    %    warning('l>2 not implemented');
    
end

